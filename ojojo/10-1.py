def cari_menu_varian(pemesanan, menu, varian):
    if pemesanan in kedai_kopi:
        pesanan = kedai_kopi[pemesanan]
        
        if menu in menu_varian:
            variants = menu_varian[menu]
            for variant in variants:
                if variant['Jenis'] == menu:
                    for v in variant['Variants']:
                        if v['Size'] == varian:
                            price = v['Price']
                            if 'Topping' in v:
                                topping = v['Topping']
                            else:
                                topping = "Tidak ada topping"
                            result = f"{pemesanan} memesan {menu} {varian} dengan harga {price} dan topping {topping}"
                            return result
                    return f"Tidak ada varian {varian} untuk menu {menu}"
            return f"Tidak ada jenis {menu} dalam daftar menu"
        else:
            return f"Tidak ada jenis {menu} dalam daftar menu"
    else:
        return f"{pemesanan} tidak ditemukan dalam daftar pesanan"

